% OTSLM/EXAMPLES example files demonstrating different features
% of OTSLM.  For beam generation, start with simple_beams.m and
% advanced_beams.m.
%
% Files
%   advanced_beams       - Example of more advanced beams (figure 3 in otslm paper)
%   calibration          - Demonstrate the SLM calibration stuff
%   checkerboard         - Checkerboard tests
%   declan_sinc          - Declan's beam script
%   dmd                  - Demonstrate the current DMD functionality of the toolbox
%   line_trap            - Simulation of different types of line traps
%   multi_beams          - Example to demonstrate combining multiple beams
%   phase_from_amplitude - Calculate the phase from a target amplitude pattern
%   qpd_slm_vis          - Simulate particles being imaged using SLM/QPD system
%   region_sampling      - Demonstrating of otslm.tools.sample_region function
%   ring_traps           - Generate ring traps
%   screen_device        - Demonstration of the screen device class (used to control SLM/DMDs)
%   simple_beams         - Example of simple beams (figure 2 in otslm paper)
%   swimmers1_beams      - Beams for swimmers 1 paper
%   swimmers1_beams2     - Generate figure for swimmers paper
%   testRingBeams        - Generate a ring beam using GS for E Coli trapping, compare to LG
%   visualise            - Demonstrate different visualisation methods
